
public class Operators {
	public static void main(String[] args) {
		
		/*
		 * Arithmetic Operator
		 * 
		 * + , -, /, *
		 */
		
		System.out.println(2+1);
		System.out.println(2-1);
		System.out.println(2*1);
		System.out.println(2/1);
		System.out.println(11%2); //return the reminder value
		
		
		/*
		 * Unary
		 * 
		 *  a + b = result;
		 */
		
		int a = 4;
		int b = 6;
		
		--a; // Pre Decrement
		b--; // Post Decrement
		
		++a; // Pre Increment
		a++; // Post Increment
		
		/*
		Bit wise?
			AND, OR , XOR, NOT           : Bit Manipulation. -> Help to reduce time complexity 
		*/
		
		/*
		 * Logical 
		 * 
		 *  use for comparission
		 *  
		 *   || _> Logical OR, && -> Logical And, ! -> Logical NOT
		 */
		
	
	}
}
