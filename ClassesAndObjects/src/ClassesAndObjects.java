
public class ClassesAndObjects {
	/*
	 * 
	 * Class :
	 * 
	 * A representation specifying the characteristics and behaviors of an object is called a class. It is not a real life entity
	 * but a template for representing real life entities.
	 * 
	 * Or 
	 * 
	 * It is a blueprint where we describe the attributes and behaviors of an real life entity.
	 * 
	 * 
	 * 
	 * Objects :
	 * 
	 * An Object which is an instance of a class is a real life entity which has some attributes and behaviors. The class determines
	 * the attributes and behaviors which an object should possess to belong to the class.
	 * 
	 * 
	 */
}
